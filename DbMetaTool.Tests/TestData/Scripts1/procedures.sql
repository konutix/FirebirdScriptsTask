CREATE OR ALTER PROCEDURE ADD_PRODUCT
 (
    P_ID INTEGER,
    P_NAME DM_NAME
)
AS
BEGIN
  INSERT INTO PRODUCTS(ID, NAME)
  VALUES (:P_ID, :P_NAME);
END;

